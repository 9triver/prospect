import axios from 'axios';

import { type IAnnualSecurityPlan } from '@/shared/model/annual-security-plan.model';

const baseApiUrl = 'api/annual-security-plans';

export default class AnnualSecurityPlanService {
  public find(id: number): Promise<IAnnualSecurityPlan> {
    return new Promise<IAnnualSecurityPlan>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IAnnualSecurityPlan): Promise<IAnnualSecurityPlan> {
    return new Promise<IAnnualSecurityPlan>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IAnnualSecurityPlan): Promise<IAnnualSecurityPlan> {
    return new Promise<IAnnualSecurityPlan>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IAnnualSecurityPlan): Promise<IAnnualSecurityPlan> {
    return new Promise<IAnnualSecurityPlan>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
