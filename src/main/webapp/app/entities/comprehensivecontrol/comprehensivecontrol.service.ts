import axios from 'axios';

import { type IComprehensivecontrol } from '@/shared/model/comprehensivecontrol.model';

const baseApiUrl = 'api/comprehensivecontrols';

export default class ComprehensivecontrolService {
  public find(id: string): Promise<IComprehensivecontrol> {
    return new Promise<IComprehensivecontrol>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: string): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IComprehensivecontrol): Promise<IComprehensivecontrol> {
    return new Promise<IComprehensivecontrol>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IComprehensivecontrol): Promise<IComprehensivecontrol> {
    return new Promise<IComprehensivecontrol>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IComprehensivecontrol): Promise<IComprehensivecontrol> {
    return new Promise<IComprehensivecontrol>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
