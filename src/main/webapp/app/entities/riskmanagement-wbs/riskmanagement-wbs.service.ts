import axios from 'axios';

import { type IRiskmanagementWbs } from '@/shared/model/riskmanagement-wbs.model';

const baseApiUrl = 'api/riskmanagement-wbs';

export default class RiskmanagementWbsService {
  public find(id: string): Promise<IRiskmanagementWbs> {
    return new Promise<IRiskmanagementWbs>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: string): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IRiskmanagementWbs): Promise<IRiskmanagementWbs> {
    return new Promise<IRiskmanagementWbs>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IRiskmanagementWbs): Promise<IRiskmanagementWbs> {
    return new Promise<IRiskmanagementWbs>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IRiskmanagementWbs): Promise<IRiskmanagementWbs> {
    return new Promise<IRiskmanagementWbs>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
