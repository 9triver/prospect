import axios from 'axios';

import { type IDeviationPermitApplication } from '@/shared/model/deviation-permit-application.model';

const baseApiUrl = 'api/deviation-permit-applications';

export default class DeviationPermitApplicationService {
  public find(id: number): Promise<IDeviationPermitApplication> {
    return new Promise<IDeviationPermitApplication>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IDeviationPermitApplication): Promise<IDeviationPermitApplication> {
    return new Promise<IDeviationPermitApplication>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IDeviationPermitApplication): Promise<IDeviationPermitApplication> {
    return new Promise<IDeviationPermitApplication>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IDeviationPermitApplication): Promise<IDeviationPermitApplication> {
    return new Promise<IDeviationPermitApplication>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
