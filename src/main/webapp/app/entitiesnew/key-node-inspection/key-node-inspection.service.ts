import axios from 'axios';

import { type IKeyNodeInspection } from '@/shared/model/key-node-inspection.model';

const baseApiUrl = 'api/key-node-inspections';

export default class KeyNodeInspectionService {
  public find(id: number): Promise<IKeyNodeInspection> {
    return new Promise<IKeyNodeInspection>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IKeyNodeInspection): Promise<IKeyNodeInspection> {
    return new Promise<IKeyNodeInspection>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IKeyNodeInspection): Promise<IKeyNodeInspection> {
    return new Promise<IKeyNodeInspection>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IKeyNodeInspection): Promise<IKeyNodeInspection> {
    return new Promise<IKeyNodeInspection>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
